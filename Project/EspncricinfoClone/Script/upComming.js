      function display(arr){
        var fixerBody = document.querySelector("#fixer-body");
          fixerBody.innerHTML = "";
        arr.map(function(elem){
            var div = document.createElement("div");

            var datetime = document.createElement("h4");
            datetime.innerText = elem.dateTime;
             
            var discription = document.createElement("p");
            discription.innerText = elem.discription
             
            var hr = document.createElement("hr");
       
            var div2 = document.createElement("div");

            var img1 = document.createElement("img");
            img1.src = elem.firstImg;

            var team1 = document.createElement("h3");
            team1.innerText = elem.firstTeamName;

            div2.append(img1,team1);
            
            var div3 = document.createElement("div");

            var img2 = document.createElement("img");
             img2.src = elem.secondImg;

            var team2 = document.createElement("h3");
            team2.innerText = elem.secondTeamName;

            div3.append(img2,team2);
             
            var matchstatus = document.createElement("p");
            matchstatus.innerText = elem.matchStatus;
             
            var bot = document.createElement("button");
            bot.textContent= "Add to Fav"


            div.append(datetime,discription,hr,div2,div3,matchstatus,bot);
            fixerBody.append(div);
        })
        
    }

   
    
